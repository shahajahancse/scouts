    //archive award dropdown
    $('#award_member_type').change(function(){
      $('.award_val').addClass('form-control input-sm');
      $(".award_val > option").remove();
      var id = $('#award_member_type').val();

      // var  full_url = hostname +"general_setting/ajax_get_district_by_div/" + id;
      //alert(full_url);

      $.ajax({
        type: "POST",
        url: hostname +"common/ajax_archive_award_by_type/" + id,
        success: function(func_data)
        {
          $.each(func_data,function(id,name)
          {
            var opt = $('<option />');
            opt.val(id);
            opt.text(name);
            $('.award_val').append(opt);
          });
        }
      });
    });

